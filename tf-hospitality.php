                                    Kalimpong, Lava, Lolegaon famous tourist spots are only again within three hours drive. 
                                    To the North is 'Dooars' meaning door to Bhutan. 
                                    Dooars is famous for numerous Tea Gardens producing world class CTC Tea, Gorumara National Forest and Jaldapara Sanctuary. 
